                                <h2>A propos du découpage</h2>
                                <br>
                                <p class="item-intro text-muted" style="text-align:justify;">

                                <br>J'ai découvert le découpage en 2006. Cela a été un véritable coup de foudre, mais aussi une promesse : j'allais retrouver un de mes grands plaisirs d'enfant. Découper du papier, le plus simplement du monde. Oui, c'est ce que j'allais faire désormais ...

	<br><br>Quand j'ai découvert la tradition du découpage suisse, j'ai tout d'abord été émerveillée de constater que cet art populaire était si vivant. A l'époque, je me trouvais « au bout d'un cycle » dans mon travail d'artiste textile, et je cherchais un mode d'expression encore plus simple et plus léger. Je rêvais d'être une artiste nomade qui n'aurait besoin que de peu de matériel et qui pourrait créer partout. Dans la réalité, je suis plutôt sédentaire mais quel bonheur de pouvoir travailler dans un tout petit espace, chez moi, ou dans une maison de vacances, ou sur le lieu même de mes expositions ! L'économie de moyens et la simplicité du découpage est pour moi synonyme d'une incroyable liberté.

	<br><br>Depuis ce jour où j'ai redécouvert le papier découpé je n'ai cessé de travailler, ou plutôt devrais-je dire, de m'amuser. C'est une grande chance que d'avoir trouvé mon mode d'expression, mon langage.

	<br><br>Jean Cocteau disait « Le découpage n'est pas un grand art mais c'est un art heureux ». Cette phrase, lue dans un journal suisse et citée par une grande artiste-découpeuse, ne s'est pas démentie pour moi non plus. Je suis heureuse quand je découpe.

	<br><br>Depuis toujours, je suis passionnée par les arts populaires. (J'ai une légère préférence pour la définition que les Américains font du « Folk Art » qui est plus large  que la définition française, car elle englobe aussi l'art naïf, entre autres. ) Je suis surtout fascinée par tout ce qu'on peut créer d'artistique et de personnel avec des matériaux simples et ordinaires. Je n'ai pas étudié aux Beaux-Arts, je suis autodidacte et la création fait depuis toujours partie de ma vie quotidienne. J'ai mis du temps à me dire que c'était ce chemin-là qui me convenait, un genre de chemin de traverse. Une école des Beaux-Arts buissonnière, faite de rencontres éblouissantes, d'essais et d'erreurs, de liberté, encore et toujours.

	<br><br>Le découpage c'est un monde qui se révèle dans une feuille de papier. C'est le cousin des théâtres d'ombres, des lanternes magiques, des guirlandes et des ombres « chinoises » que l'on fait le soir sur les murs de la maison . Il y a des découpeurs dans les rues des grandes villes qui peuvent faire votre silhouette : à Montmartre, dans le métro de New-York, et partout en Asie bien sûr. C'est un art modeste mais ses applications sont infinies !

	<br><br>Sur le plan technique, je travaille comme beaucoup de découpeurs traditionnels : je dessine au dos de la feuille -en miroir bien sûr- le motif ou le tableau, que je découpe ensuite avec un cutter la plupart du temps, et parfois aux ciseaux. Mon outillage est donc minimal ! J'aime découper le papier noir dit « Silhouette » ou bien le blanc. La feuille est pour moi un grand espace de narration et de liberté et je n'aime rien tant que de raconter des histoires. Petit à petit, j'ai appris à surmonter les difficultés techniques liées à cet art, j'ai trouvé ma « méthode ». Comme les graveurs je travaille en songeant sans cesse à ce que je dois enlever comme matière, et en évitant le geste fatal qui gâcherait le résultat final ! Mais l'instant magique où je retourne ma feuille de papier pour voir mon travail terminé, avec la lumière qui passe au travers, vaut pour moi tous les efforts.

                                </p>
                                <br>
